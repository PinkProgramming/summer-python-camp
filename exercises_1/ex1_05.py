"""
What are the results of the following operations? Why?
1.5 + 2
1.5 // 2.0
1.5 / 2.0
1.5 ** 2
1 / 2
-3 // 2

"""

# all are simple math operations
print("The result of: 1.5 + 2 =", 1.5 + 2, type(1.5 + 2))
print("The result of: 1.5 // 2.0 =", 1.5 // 2.0, type(1.5 // 2.0))  # integer division on 2 floats is still a float
print("The result of: 1.5 / 2.0 =", 1.5 / 2.0, type(1.5 / 2.0))
print("The result of: 1.5 ** 2 =", 1.5 ** 2, type(1.5 ** 2))
print("The result of: 1 / 2 =", 1 / 2, type(1 / 2))
print("The result of: -3 // 2 =", -3 // 2, type(-3 // 2))
